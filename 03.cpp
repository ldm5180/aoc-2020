constexpr auto line_length(std::string_view input) {
  return std::distance(std::begin(input),
                       std::find(std::begin(input), std::end(input), '\n'));
}

constexpr auto line_count(std::string_view input) {
  return 1 + std::count_if(std::begin(input), std::end(input),
                           [](const auto c) { return c == '\n'; });
}

template <auto num_lines> constexpr auto lines_array(std::string_view input) {
  std::array<std::string_view, num_lines> lines{};
  std::size_t start{};
  std::size_t end{};

  for (auto i = 0u; i < num_lines && end != std::string_view::npos; ++i) {
    end = input.find_first_of('\n', start);
    lines[i] = input.substr(start, end - start);
    start = end + 1;
  }

  return lines;
}

struct Location {
  std::size_t x{};
  std::size_t y{};
};

template<auto right = 3, auto down = 1>
constexpr auto tree_count() {
  Location loc{.x = right, .y = down};
  auto count = 0;

  constexpr auto num_lines = line_count(input);
  constexpr auto lines = lines_array<num_lines>(input);
  constexpr auto line_len = line_length(input);

  while (loc.y < num_lines) {
    auto spot = lines[loc.y][loc.x % line_len];
    if (spot == '#') {
      ++count;
    }
    loc.x += right;
    loc.y += down;
  }
  return count;
}

